-- =============================================================================
-- FUNCTION: normalize_status(raw text) RETURNS normalized_status
--
-- Maps the messy raw status strings from the Google Sheet to the canonical
-- normalized_status enum. Called by Apps Script during upserts.
--
-- Matching order matters: more specific patterns first.
-- =============================================================================
CREATE OR REPLACE FUNCTION normalize_status(raw TEXT)
RETURNS normalized_status
LANGUAGE plpgsql
IMMUTABLE
PARALLEL SAFE
AS $$
DECLARE
    lower_raw TEXT;
BEGIN
    IF raw IS NULL OR TRIM(raw) = '' THEN
        RETURN 'unknown';
    END IF;

    lower_raw := LOWER(TRIM(raw));

    -- Sent variants: "Enviado ✅", "Enviado", "ENVIADO", "enviado ", "SENT", etc.
    IF lower_raw LIKE 'enviado%'
       OR lower_raw = 'sent'
       OR lower_raw = 'completado'
    THEN
        RETURN 'sent';
    END IF;

    -- Sending / in progress
    IF lower_raw = 'procesando'
       OR lower_raw = 'sending'
    THEN
        RETURN 'sending';
    END IF;

    -- Ready to send
    IF lower_raw LIKE '%listo para enviar%'
       OR lower_raw LIKE '%ready%'
    THEN
        RETURN 'pending_ready';
    END IF;

    -- Blocked by too many banks (validation rule: > 3 banks detected)
    -- Examples: "5 banks detected → blocked (> 3)", "4 banks detected → blocked (> 3)"
    IF lower_raw LIKE '%banks detected%blocked%'
       OR lower_raw LIKE '%blocked%'
    THEN
        RETURN 'blocked_validation';
    END IF;

    -- Failed: draft generated (not actually sent), or hard errors
    IF lower_raw LIKE '%borrador%'
       OR lower_raw LIKE '%no se ha enviado%'
    THEN
        RETURN 'failed';
    END IF;

    -- Failed: system/config errors
    IF lower_raw LIKE '%error%'
       OR lower_raw LIKE '%no existe%'
       OR lower_raw LIKE '%no tenemos correo%'
    THEN
        RETURN 'failed';
    END IF;

    -- Offer received
    IF lower_raw LIKE '%oferta%'
       OR lower_raw LIKE '%offer%received%'
    THEN
        RETURN 'offer_received';
    END IF;

    -- Rejected
    IF lower_raw LIKE '%denegad%'
       OR lower_raw LIKE '%rechazad%'
       OR lower_raw LIKE '%rejected%'
    THEN
        RETURN 'rejected';
    END IF;

    -- More information requested
    IF lower_raw LIKE '%más información%'
       OR lower_raw LIKE '%mas informacion%'
       OR lower_raw LIKE '%more info%'
       OR lower_raw LIKE '%pendiente documentacion%'
    THEN
        RETURN 'more_info_requested';
    END IF;

    -- Relaunch requested
    IF lower_raw LIKE '%relanzar%'
       OR lower_raw LIKE '%relaunch%'
    THEN
        RETURN 'relaunch_requested';
    END IF;

    -- Blocked by red flag (explicit red flag mention)
    IF lower_raw LIKE '%red flag%'
    THEN
        RETURN 'blocked_red_flag';
    END IF;

    -- Blocked by missing documents
    IF lower_raw LIKE '%documentos%'
       OR lower_raw LIKE '%missing%doc%'
    THEN
        RETURN 'blocked_missing_docs';
    END IF;

    -- Ver notas / see notes — treat as unknown until manually reviewed
    IF lower_raw LIKE '%ver nota%'
       OR lower_raw LIKE '%see note%'
    THEN
        RETURN 'unknown';
    END IF;

    RETURN 'unknown';
END;
$$;

COMMENT ON FUNCTION normalize_status(TEXT) IS
    'Maps raw Sheet status strings to the normalized_status enum. '
    'Called during Apps Script upserts. Add new patterns here as new raw values appear in production.';


-- =============================================================================
-- FUNCTION + TRIGGER: update_updated_at
-- Automatically refreshes sheet_rows.updated_at on every UPDATE.
-- =============================================================================
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_sheet_rows_updated_at
    BEFORE UPDATE ON sheet_rows
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TRIGGER trg_sheet_rows_updated_at ON sheet_rows IS
    'Automatically sets updated_at = NOW() on every row update.';


