-- =============================================================================
-- TABLE: sheet_rows
-- One row per (bank, opportunity_id) pair — mirrors every row of every Sheet tab.
-- This is the central fact table. Apps Script upserts here on every sync.
--
-- CRITICAL CONSTRAINT: UNIQUE(bank_id, opportunity_id)
--   Prevents duplicate submissions per bank. Any attempt to insert a second row
--   for the same bank+opportunity must be handled as an UPDATE, not an INSERT.
-- =============================================================================
CREATE TABLE IF NOT EXISTS sheet_rows (
    id                      UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Source identifiers
    uid                     TEXT            UNIQUE,             -- ITEM ID from Apps Script (nullable for banks without UID column)
    bank_id                 INT             NOT NULL REFERENCES banks(id) ON DELETE RESTRICT,
    opportunity_id          BIGINT          NOT NULL,           -- Pipedrive Opportunity ID
    bank_deal_id            BIGINT,                             -- ID of deal at the bank's system

    -- Client & loan data
    nombre_cliente          TEXT            NOT NULL,
    importe                 NUMERIC(12, 2),                     -- loan amount in EUR
    link_dossier            TEXT,                               -- Google Drive URL

    -- Timing
    timestamp_entry         TIMESTAMPTZ,                        -- when the row was entered in the Sheet
    send_trigger            BOOLEAN,                            -- normalized from "Enviado/Enviar": Yes→true, No/blank→false/null
    timestamp_sent          TIMESTAMPTZ,                        -- when Apps Script marked it as sent

    -- Status (dual: raw for auditability, normalized for queries)
    status_raw              TEXT,                               -- original value from Sheet, never overwritten
    status                  normalized_status,                  -- computed normalized state

    -- Red flags (dual: raw string and parsed array)
    red_flags_raw           TEXT,                               -- original pipe-separated string from Sheet
    red_flags               TEXT[],                             -- array: each element is one trimmed red flag text

    -- Bank-specific optional columns
    clasificacion           TEXT,
    dossier                 TEXT,
    auto_bayteca            TEXT,                               -- 'NO' or empty
    auto_banco              TEXT,
    autorizacion            TEXT,
    autorizacion_link       TEXT,
    autorizacion_red_flag   TEXT,
    process_status          TEXT,                               -- 'Completado', 'Procesando', 'Listo para enviar'
    notas                   TEXT,

    -- Response data
    fecha_respuesta         DATE,
    dias_sin_respuesta      INT,

    -- Operational metadata
    owner                   TEXT,
    sheet_row_number        INT,                                -- source row number in the Sheet (for traceability and resyncs)
    synced_at               TIMESTAMPTZ     NOT NULL DEFAULT NOW(), -- last time Apps Script touched this row
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    -- CRITICAL: prevents two rows for the same bank+opportunity (anti-duplicate guard)
    CONSTRAINT uq_bank_opportunity UNIQUE (bank_id, opportunity_id)
);

COMMENT ON TABLE  sheet_rows IS 'Mirror of every row across all bank Sheet tabs. Central fact table. Apps Script upserts here.';
COMMENT ON COLUMN sheet_rows.uid IS 'ITEM ID generated by Apps Script. NULL for banks that do not have this column.';
COMMENT ON COLUMN sheet_rows.status_raw IS 'Original unmodified status string from the Sheet. Never overwritten once set, for auditability.';
COMMENT ON COLUMN sheet_rows.status IS 'Normalized status computed from status_raw via normalize_status(). Safe for filtering and grouping.';
COMMENT ON COLUMN sheet_rows.red_flags_raw IS 'Original pipe-separated red flag text from the Sheet. Preserved verbatim.';
COMMENT ON COLUMN sheet_rows.red_flags IS 'Parsed array of individual red flag strings split on | and trimmed.';
COMMENT ON COLUMN sheet_rows.send_trigger IS 'TRUE = row has Enviar/Enviado = Yes. FALSE = explicitly No. NULL = blank/unknown.';
COMMENT ON COLUMN sheet_rows.sheet_row_number IS 'Row number in the Google Sheet (1-indexed). Used by Apps Script to update specific cells.';
COMMENT ON CONSTRAINT uq_bank_opportunity ON sheet_rows IS 'Core anti-duplicate guard. One row per bank per Pipedrive opportunity.';


-- =============================================================================
-- TABLE: dispatch_attempts
-- Records every attempt (successful or not) to dispatch a row to a bank.
-- Append-only. Never update or delete rows here.
-- =============================================================================
CREATE TABLE IF NOT EXISTS dispatch_attempts (
    id              UUID            PRIMARY KEY DEFAULT gen_random_uuid(),
    sheet_row_id    UUID            NOT NULL REFERENCES sheet_rows(id) ON DELETE RESTRICT,
    bank_id         INT             NOT NULL REFERENCES banks(id) ON DELETE RESTRICT,
    opportunity_id  BIGINT          NOT NULL,
    uid             TEXT,                                       -- UID at time of dispatch (snapshot)

    -- Who/what triggered this attempt
    triggered_by    TEXT            NOT NULL,                   -- 'apps_script' | 'manual_relaunch' | 'auto_retry'

    -- Outcome
    status          TEXT            NOT NULL,                   -- 'sent' | 'failed' | 'blocked' | 'duplicate_prevented'
    webhook_url     TEXT,                                       -- destination URL called
    response_body   TEXT,                                       -- raw response from webhook/bank system
    error_message   TEXT,                                       -- error detail on failure

    -- Relaunch guard: if force_flag=true the caller confirmed they want to resend
    force_flag      BOOLEAN         NOT NULL DEFAULT FALSE,

    dispatched_at   TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    created_at      TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE  dispatch_attempts IS 'Append-only log of every dispatch attempt. Never delete rows.';
COMMENT ON COLUMN dispatch_attempts.force_flag IS 'TRUE means the caller explicitly passed force=true, acknowledging a potential re-send.';
COMMENT ON COLUMN dispatch_attempts.triggered_by IS 'apps_script | manual_relaunch | auto_retry. Used for audit and metrics.';
COMMENT ON COLUMN dispatch_attempts.status IS 'sent | failed | blocked | duplicate_prevented.';


